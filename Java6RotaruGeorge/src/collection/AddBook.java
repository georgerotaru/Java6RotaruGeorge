package collection;

/**
 *
 * @author George
 */
public class AddBook {

private String isbn;
private String title;

    public AddBook(String bookNo, String bookTitle) {
        isbn = bookNo;
        title = bookTitle;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
}
